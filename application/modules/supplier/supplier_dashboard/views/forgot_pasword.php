function changepwd()
{
  otp=$("#otp").val();
  n_password=$("#n_password").val();
  c_password=$("#c_password").val();

  if(otp>0 && n_password!="" && c_password!="")
  {
      if(n_password==c_password)
      {
          keyval={otp:otp,n_password:n_password,c_password:c_password,type:2}
        $.ajax({
           type: "POST",
           url: '<?php echo base_url() ?>home/forgotpaswrdManage',
           data: keyval ,
           success: function(result){
                if(result==1)
                {
                    $("#error").html("Yor password has been reset, Now try login");
                    setTimeout(function() {
                          window.open("<?php echo base_url(); ?>",'_self');
                      }, 3000);
                }
                else {
                    $("#error").html(result);
                }
             }
        });
      }
      else {
        $("#error").html("New password and confirm an password do not match");
      }
  }
  else {
      $("#error").html("You must fill all coloumns");
  }

}
function checkuser()
{
  email=$("#email").val();

  if(email)
  {
    $("#btn").html("Please wait...");
    $("#btn").attr("disabled",true);
    keyval={email:email,type:1}
      $.ajax({
         type: "POST",
         url: '<?php echo base_url() ?>home/forgotpaswrdManage',
         data: keyval ,
         success: function(result){
              if(result==1)
              {
                  $("#email_block").hide();
                  $("#gtop").hide();
                  $("#chgpwd").show();
                  $("#pwd_block").show();
                  $("#error").html("An otp has been sent to "+email+" Please enter it to reset your password");
              }
              else {
                $("#error").html(result);
              }

              $("#btn").html("GET OTP");
              $("#btn").attr("disabled",false);
           }
      });
  }
  else {
    $("#error").html("You must enter your email id");

  }
}
